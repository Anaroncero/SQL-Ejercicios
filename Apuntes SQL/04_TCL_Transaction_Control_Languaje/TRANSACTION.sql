Transaction TCL: Se refiere a las instrucciones que gestiona una base de datos. Son conjuntos de 
operaciones que se ejecutan en bloque. para asegurar la integridad y consistencia de los datos

Principales comandos de TCL son:
-COMMIT: Finaliza una transaccion y guarda todas las modificaciones de la transaccion.
-ROLLBACK: Revierte todas las modificaciones realizadas en la transaccion. Deshace todos los cambios del commit
-SAVEPOINT: Crea un punto de restauracion dentro de una transaccion. Permite volver con ROLLBACK a ese punto (a los datos de ese momento)

